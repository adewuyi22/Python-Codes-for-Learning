#print the values of an array
cars = ["Ford", "Volvo", "BMW"]
print("My favorite cars are: ", cars)


#Print the length of an array
cars = ["Ford", "Volvo", "BMW"]
print(len(cars))


#loop through an array
cars = ["Ford", "Volvo", "BMW"]
for x in cars:
    print(x)


#Add to the elements of an array
cars = ["Ford", "Volvo", "BMW"]
cars.append("Benz")
print(cars)


#Remove from the elements of an array
cars = ["Ford", "Volvo", "BMW"]
cars.remove("BMW")
print(cars)


#Using "pop" method to Remove from the elements of an array
cars = ["Ford", "Volvo", "BMW"]
cars.pop(1)
print(cars)

#Sort method will arrange from ascending to descending.
cars = ["Ford", "Volvo", "BMW"]
cars.sort()
print(cars)


#Printing in reverse form
cars = ["apple", "banana", "carrot"]
cars.reverse()
print(cars)





